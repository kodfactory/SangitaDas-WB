# Arithmatic operators: + - / % * // **

a = 10 + 3 
b = 10 - 3
c = 10 * 3
d = 10 / 3
e = 10 % 3 # modulas -> it will give the reminder
f = 10 // 3 # it will give us the floor value
g = 10 ** 3 # it will do 10 to the power 3

print(a) # 13
print(b) # 7
print(c) # 30
print(d) # 3.33
print(e) # 1
print(f) # 3
print(g) # 1000

print(19 ** 2)
